#include <iostream>
using namespace std;


void Program5()
{
	int num;

	num = 1;
	cout << "num = " << num << endl;
	cout << "Increment Operator before num = " << ++num << endl;
	cout << "num after = " << num << endl;

	num = 1;
	cout << "num = " << num << endl;
	cout << "Increment Operator after num = " << num++ << endl;
	cout << "num after = " << num << endl;

	num = 1;
	cout << "num = " << num << endl;
	cout << "Decrement Operator after num = " << --num << endl;
	cout << "num after << num << endl;" << endl;

	num = 1;
	cout << "num = << num << endl;" << num << endl;
	cout << "Decrement Operator after num =" << num-- << endl;
	cout << "num after = " << num << endl;

	return;
}
